    private void ButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSimpanActionPerformed
    Frame2 i = new Frame2 ();
i.txtNama.setText (txtNama.getText());
i.txtTempatLahir.setText (txtTempatLahir.getText());
i.txtTanggalLahir.setText (txtTanggalLahir.getText());
if (rbPerempuan.isSelected()){
    i.txtJk.setText("Perempuan");
}
if (rbLakiLaki.isSelected()){
    i.txtJk.setText("Laki Laki");
}
i.txtAlamat.setText (txtAlamat.getText());
i.txtNoTelp.setText (txtNoTelp.getText());
i.txtEmail.setText (txtEmail.getText());
String n = (String)cbProdi.getSelectedItem();
i.txtProdi.setText(n);
i.txtFakultas.setText (txtFakultas.getText());
if(cbxCentang.isSelected()){
   if(txtNama.getText().isEmpty()
           || txtTempatLahir.getText().isEmpty()
           || txtTanggalLahir.getText().isEmpty()
           || buttonGroup1.equals(false)
           || txtAlamat.getText().isEmpty()
           || txtNoTelp.getText().isEmpty()
           || txtEmail.getText().isEmpty()
           || txtFakultas.getText().isEmpty()
           )
{JOptionPane.showMessageDialog(null, "Data Belum Lengkap");
                   
}
   else{
       i.setVisible(true);
       dispose();
   }

}  else {
           JOptionPane.showMessageDialog(null,"Pastikan Data Anda Benar!!!");
            }
        
// TODO add your handling code here:
    }//GEN-LAST:event_ButtonSimpanActionPerformed

